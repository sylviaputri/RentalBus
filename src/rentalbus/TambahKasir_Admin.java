    public String getIdKasir() throws SQLException{
        String id="";
        kon.connect();
        kon.createQuery("select id_kasir from kasir ORDER BY id_kasir");
        while(kon.myRs.next()){
            id = String.valueOf(kon.myRs.getInt("id_kasir")+1);
        }
        return id;
    }

